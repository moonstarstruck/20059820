    <h3>What is SDG?</h3>
    <hr>
    <p>The Sustainable Development Goals (SDGs) or Global Goals are a collection of 17 interlinked global goals
      designed
      to be a "shared blueprint for peace and prosperity for people and the planet, now and into the future".
      The SDGs were set up in 2015 by the United Nations General Assembly (UN-GA) and are intended to be achieved
      by
      2030. They are included in a UN-GA Resolution called the 2030 Agenda or what is colloquially known as Agenda
      2030. The SDGs were developed in the Post-2015 Development Agenda as the future global development framework
      to succeed the Millennium Development Goals which were ended in 2015. The SDGs emphasize the interconnected
      environmental, social and economic aspects of sustainable development, by putting sustainability at their
      center.

      The 17 SDGs are: No poverty, zero hunger, good health and well-being, quality education, gender equality,
      clean
      water and sanitation, affordable and clean energy, decent work and economic growth, industry, innovation and
      infrastructure, Reduced Inequality, Sustainable Cities and Communities, Responsible Consumption and
      Production,
      Climate Action, Life Below Water, Life On Land, Peace, Justice, and Strong Institutions, Partnerships for
      the
      Goals. Though the goals are broad and interdependent, two years later (6 July 2017), the SDGs were made more
      "actionable" by a UN Resolution adopted by the General Assembly. The resolution identifies specific targets
      for
      each goal, along with indicators that are being used to measure progress toward each target. The year by
      which the target is meant to be achieved is usually between 2020 and 2030. For some of the targets, no end
      date is given.</p>
  </article>

